-- パスワード
CREATE TABLE IF NOT EXISTS PASSWORD ( ID INT NOT NULL,PASSWORD VARCHAR(20) NOT NULL );

-- 記録
CREATE TABLE IF NOT EXISTS RECORD ( ID INT NOT NULL AUTO_INCREMENT,BOARD_TITLE VARCHAR(20) NOT NULL, PLAYER INT NOT NULL, REMARK VARCHAR(20) );

-- ボードゲーム
CREATE TABLE IF NOT EXISTS BOARD_GAME( TITLE VARCHAR(20) NOT NULL, PLAYER INT NOT NULL, OVERVIEW VARCHAR(50));
-- 場所
CREATE TABLE IF NOT EXISTS PLACE (NAME VARCHAR(20));

