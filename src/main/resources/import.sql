-- ユーザ
DROP TABLE USER;
CREATE TABLE IF NOT EXISTS USER ( ID INT NOT NULL AUTO_INCREMENT, NAME VARCHAR(20) NOT NULL , AUTHORITY INT NOT NULL);
-- パスワード
DROP TABLE PASSWORD;
CREATE TABLE IF NOT EXISTS PASSWORD ( USER_ID INT NOT NULL,PASSWORD VARCHAR(20) NOT NULL );
-- ルーム
DROP TABLE ROOM;
CREATE TABLE IF NOT EXISTS ROOM ( ID INT NOT NULL AUTO_INCREMENT,ROOM_NAME VARCHAR(20) NOT NULL, BOARD_TITLE VARCHAR(20) NOT NULL, PLACE_NAME VARCHAR(20), PLAYER INT NOT NULL, REMARK VARCHAR(20), CREATE TIMESTAMP NOT NULL);
-- 参加
DROP TABLE JOIN_ROOM;
CREATE TABLE IF NOT EXISTS JOIN_ROOM (USER_ID INT NOT NULL, ROOM_ID INT NOT NULL, OWNER BOOLEAN, JOIN_DATE TIMESTAMP NOT NULL, PRIMARY KEY(USER_ID, ROOM_ID));
-- 結果
DROP TABLE RESULT;
CREATE TABLE IF NOT EXISTS RESULT ( USER_ID INT NOT NULL, ROOM_ID INT NOT NULL, RANK INT NOT NULL, SCORE VARCHAR(20), COMMENT VARCHAR(20), PRIMARY KEY(USER_ID, ROOM_ID));
-- ボードゲーム
DROP TABLE BOARD_GAME;
CREATE TABLE IF NOT EXISTS BOARD_GAME( TITLE VARCHAR(20) NOT NULL, PLAYER INT NOT NULL, OVERVIEW VARCHAR(50));
-- 場所
DROP TABLE PLACE;
CREATE TABLE IF NOT EXISTS PLACE (NAME VARCHAR(20), URL VARCHAR(50));
-- チェックイン
DROP TABLE CHECK_IN;
CREATE TABLE IF NOT EXISTS CHECK_IN(ID INT NOT NULL AUTO_INCREMENT, USER_ID INT NOT NULL, PLACE_NAME VARCHAR(20) NOT NULL, TIMESTAMP TIMESTAMP NOT NULL, CHECKED_OUT BOOLEAN);

